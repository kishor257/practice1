package programmingPractice.programsOnNumbers;

public class SwapTwoNumbersUsingThirdVariable2 {
	
	public static void main(String[] args) {
		
		int a=20;
		int b=10;
		int temp;
		
		System.out.println("Before swap: "+a+" "+b);
		temp=a;
		a=b;
		b=temp;		
		System.out.println("Before swap: "+a+" "+b);
	}

}
